    public void limpar() {
        inputTitulo.setText("");
        inputNPaginas.setText("");
        cbEditora.setSelectedIndex(0);
    }

    public void cadastrar() {
        Arquivo arq = new Arquivo();
        Livro l1 = new Livro();

        try {
            if (!inputTitulo.getText().equals("") && !inputNPaginas.getText().equals("")) {
                l1.titulo = inputTitulo.getText();
                l1.qtdPaginas = Integer.parseInt(inputNPaginas.getText());
                l1.editora = (String) cbEditora.getSelectedItem();
                arq.escrever(l1);
            } else {
                throw new CampoVazioException("Preencha os campos corretamente!");
            }
        } catch (CampoVazioException e) {
            System.out.println(e);
        }
        limpar();
    }

    public void listar() {
        Arquivo arq = new Arquivo();
        ArrayList<Livro> livros = arq.ler();
        String[] titulos = new String[livros.size()];
        String[] nPaginas = new String[livros.size()];
        String[] editoras = new String[livros.size()];

        for (int i = 0; i < livros.size(); i++) {
            titulos[i] = livros.get(i).titulo;
            nPaginas[i] = Integer.toString(livros.get(i).qtdPaginas);
            editoras[i] = livros.get(i).editora;
        }

        listaTitulo.setListData(titulos);
        listaPagina.setListData(nPaginas);
        listaEditora.setListData(editoras);

    }
